// Criando as variáveis
let nome;
let idade;
let cidade;

// Solicitando ao usuário que insira os valores
nome = prompt("Por favor, insira seu nome:");
idade = prompt("Por favor, insira sua idade:");
cidade = prompt("Por favor, insira sua cidade:");

// Exibindo as informações formatadas
console.log(`Nome: ${nome}, Idade: ${idade}, Cidade: ${cidade}`);

// Ou, se preferir exibir em um alert:
alert(`Nome: ${nome}\nIdade: ${idade}\nCidade: ${cidade}`);